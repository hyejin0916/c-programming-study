#include <stdio.h>

int main(void)
{
    int x = 10;
    int y = 20;
    printf("x = %d입니다.\n", x);
    printf("y = %d입니다.\n", y);
    printf("x + y = %d입니다.\n", x + y);
    printf("x - y = %d입니다.\n", x - y);
    printf("x * y = %d입니다.\n", x * y);
    printf("x / y = %d입니다.\n", x / y); //c언어에서 나누기를 하면 몫만 나옴
    return 0;
}